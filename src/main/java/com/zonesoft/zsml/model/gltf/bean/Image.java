package com.zonesoft.zsml.model.gltf.bean;

public class Image {
	private String uri;
	private String mimeType;
	private int bufferView;
	private String name;

	public Image() {

	}

	public String getUri() {
		return uri;
	}

	public String getMimeType() {
		return mimeType;
	}

	public int getBufferView() {
		return bufferView;
	}

	public String getName() {
		return name;
	}
}
